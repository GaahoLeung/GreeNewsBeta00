package cn.edu.cqu.greenewsbeta01;

public class Like {
    private String name;
    private int imageId;
    public Like(String name, int imageId) {
        this.name = name;
        this.imageId = imageId;
    }
    public String getName() {
        return name;
    }
    public int getImageId() {
        return imageId;
    }
}
